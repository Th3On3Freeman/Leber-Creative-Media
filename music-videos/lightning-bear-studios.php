                                            <h2>We're not using the Z-word! (2019)</h2>
                                            <hr class="star-light">
                                            <span class="skills">AWA Thunderdome 2019 Contest Winner</span>
                                            
                                            <p class="centerText"><a href="https://www.animemusicvideos.org/members/members_videoinfo.php?v=206700" target="_blank">AMV.org Link</a></p>
                                            
                                            <p class="mediaDesc">This was a video that I made for <a href="http://awa-con.com/" target="_blank">Anime Weekend Atlanta's</a> <a href="http://thunderdome.videoarttrack.com/" target="_blank">Thunderdome Event</a> in 2019 with <a href="http://www.animemusicvideos.org/members/members_myprofile.php?user_id=1087909" target="_blank">SasukeChanX</a>, <a href="http://www.animemusicvideos.org/members/members_myprofile.php?user_id=1117120" target="_blank">Speedy180</a>, <a href="https://www.animemusicvideos.org/members/members_myprofile.php?user_id=1170422" target="_blank">TwigglyFiggly</a> and <a href="https://www.animemusicvideos.org/members/members_myprofile.php?user_id=1108" target="_blank">Lord Rae</a>.</p>

                                            <p class="mediaDesc">This song was chosen to help fit in with the Halloween theme AWA has had with its date change in recent years. The division was as follows:</p>
                                            
                                            <ul class="list-group">
                                                <li class="list-group-item"><a href="https://youtu.be/ex0n9tNbPKs" target="_blank"><p class="list-group-item-text">TwigglyFiggly/Dexter (0:00-1:04)</p></a></li>
                                                <li class="list-group-item"><a href="https://youtu.be/ex0n9tNbPKs?t=64" target="_blank"><p class="list-group-item-text">Lord Rae (1:04-2:19)</p></a></li>
                                                <li class="list-group-item"><a href="https://youtu.be/ex0n9tNbPKs?t=139" target="_blank"><p class="list-group-item-text">Speedy180 (2:19-2:47)</p></a></li> 
                                                <li class="list-group-item"><a href="https://youtu.be/ex0n9tNbPKs?t=168" target="_blank"><p class="list-group-item-text">SasukeChanX (2:48-3:30)</p></a></li>
                                                <li class="list-group-item"><a href="https://youtu.be/ex0n9tNbPKs?t=211" target="_blank"><p class="list-group-item-text">Dexter (3:31-4:15)</p></a></li>
                                            </ul>
                                                    
                                            <p>I was the last one to edit so I had an extra hour to pull everything together, including filling in some gaps in <i>TwigglyFiggly's</i> footage and conforming the aspect ratios from <i>Lord Rae's</i> footage to match the rest of ours.</p>
                                            
                                            <p>Beyond that I also added transitions between each of the sections and used <i>Annihilation</i> (notably a live-action source) as my choice to help differentiate my section and add a different flavor to the video.</p>
                                            
                                            <p class="mediaDesc">Overall, an interesting combination and experiment in damage control when plans go awry.</p>
                                            
